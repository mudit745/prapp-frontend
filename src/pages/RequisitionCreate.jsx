  const handleRemoveDocument = async (docId) => {
    const doc = documents.find(d => d.id === docId)
    if (!doc) return

    // Check if this is a document from the database
    // Documents loaded from DB have UUIDs (not starting with "doc-"), newly uploaded ones have temp IDs like "doc-1234567890-abc"
    // Also check if document has storagePath property which is set when loaded from DB
    // UUID format: 8-4-4-4-12 characters (e.g., "3aa3aef6-5de6-4936-905e-1ad492f7713e")
    const isUUID = doc.id && /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(doc.id)
    const isDatabaseDocument = doc.storagePath || isUUID || (doc.id && !doc.id.startsWith('doc-') && doc.id.includes('-') && doc.id.length > 30)
    const documentId = doc.document_id || (isDatabaseDocument ? doc.id : null)

    // If document exists in database, delete it via API
    if (documentId && editingPR?.pr_id) {
      try {
        const user = JSON.parse(localStorage.getItem('user') || '{}')
        await axios.delete(`/api/v1/documents/${documentId}`, {
          headers: {
            'X-User-ID': user.employee_id || user.user_id || '',
            'X-Tenant-ID': user.tenant_id || ''
          }
        })
      } catch (error) {
        console.error('Error deleting document from database:', error)
        // Continue with removal even if API call fails
      }
    }

    // Delete from Firebase Storage
    if (doc.url) {
      try {
        // Extract storage path from URL
        // Firebase Storage URLs are in format: https://firebasestorage.googleapis.com/v0/b/BUCKET/o/PATH?alt=media&token=...
        const urlObj = new URL(doc.url)
        const pathMatch = urlObj.pathname.match(/\/o\/(.+)\?/)
        if (pathMatch) {
          const encodedPath = pathMatch[1]
          const storagePath = decodeURIComponent(encodedPath)
          const storageRef = ref(storage, storagePath)
          await deleteObject(storageRef)
        }
      } catch (error) {
        console.error('Error deleting file from storage:', error)
        // Continue with removal even if storage delete fails
      }
    }

    // Remove from local state
    setDocuments(prev => prev.filter(d => d.id !== docId))
  }

